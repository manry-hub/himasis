    <script>
        function initCarousel(carouselId) {
            let currentIndex = 0;
            const carouselContainer = document.getElementById(carouselId);
            const carouselContent = carouselContainer.querySelector('.carousel-content');
            const totalItems = carouselContainer.querySelectorAll('.carousel-item').length;

            function showSlide(index) {
                // Infinite loop: check index bounds
                if (index >= totalItems) {
                    currentIndex = 0;
                } else if (index < 0) {
                    currentIndex = totalItems - 1;
                } else {
                    currentIndex = index;
                }

                // Set slide based on current index
                carouselContent.style.transform = `translateX(-${currentIndex * 100}%)`;
            }

            // Event listeners for next and previous buttons
            carouselContainer.querySelector('.prevButton').addEventListener('click', function() {
                showSlide(currentIndex - 1);
            });

            carouselContainer.querySelector('.nextButton').addEventListener('click', function() {
                showSlide(currentIndex + 1);
            });

            // Initial slide
            showSlide(currentIndex);
        }

        // Inisialisasi carousel yang berbeda
        initCarousel('carousel-inti');
        initCarousel('carousel-pendidikan');
        initCarousel('carousel-mibat');
        initCarousel('carousel-kesma');
        initCarousel('carousel-humas');
    </script>

